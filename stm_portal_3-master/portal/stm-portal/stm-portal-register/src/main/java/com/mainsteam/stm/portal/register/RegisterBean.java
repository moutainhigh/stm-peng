package com.mainsteam.stm.portal.register;

public class RegisterBean {

	String portalUrl = null;
	String portalWsdl = null;
	String itmUrl = null;
	String itmWsdl = null;
	String codeFlag = null;

	public String getPortalUrl() {
		return portalUrl;
	}

	public void setPortalUrl(String portalUrl) {
		this.portalUrl = portalUrl;
	}

	public String getPortalWsdl() {
		return portalWsdl;
	}

	public void setPortalWsdl(String portalWsdl) {
		this.portalWsdl = portalWsdl;
	}

	public String getItmUrl() {
		return itmUrl;
	}

	public void setItmUrl(String itmUrl) {
		this.itmUrl = itmUrl;
	}

	public String getItmWsdl() {
		return itmWsdl;
	}

	public void setItmWsdl(String itmWsdl) {
		this.itmWsdl = itmWsdl;
	}

	public String getCodeFlag() {
		return codeFlag;
	}

	public void setCodeFlag(String codeFlag) {
		this.codeFlag = codeFlag;
	}

}
