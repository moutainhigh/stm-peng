package com.mainsteam.stm.portal.alarm.web.vo;

public class SpecialAlarmParameterBean {
	private String prentCategoryId;
	private String childCategoryId;
	private String alarmType;
	
	public String getPrentCategoryId() {
		return prentCategoryId;
	}
	public void setPrentCategoryId(String prentCategoryId) {
		this.prentCategoryId = prentCategoryId;
	}
	public String getChildCategoryId() {
		return childCategoryId;
	}
	public void setChildCategoryId(String childCategoryId) {
		this.childCategoryId = childCategoryId;
	}
	public String getAlarmType() {
		return alarmType;
	}
	public void setAlarmType(String alarmType) {
		this.alarmType = alarmType;
	}
}
