package com.mainsteam.stm.dsl.expression.exception;

public class OCELException extends Exception {
    public OCELException() {
        super();
    }

    public OCELException(String message) {
        super(message);
    }

    public OCELException(String message, Throwable cause) {
        super(message, cause);
    }

    public OCELException(Throwable cause) {
        super(cause);
    }

}
